public class SpecialCharEx {
    public static void main(String[] args) {
        System.out.println('\'');               // `
        System.out.println("abc\t123\b456");    // abc  12456
        System.out.println('\n');               // 개행
        System.out.println("\"Hello\"");        // "Hello"
        System.out.println("C:\\");             // C:\
    }
}
